export const getProductById = (id: string): ProductData | undefined => {
  return products.find(product => product.id === id);
};

export const getProductsByCategory = (category: string): ProductData[] => {
  return products.filter(product => product.category === category);
};

export const getProductsBySubcategory = (subcategory: string): ProductData[] => {
  return products.filter(product => product.subcategory === subcategory);
};

export const getProductsByBrand = (brand: string): ProductData[] => {
  return products.filter(product => product.brand === brand);
};
